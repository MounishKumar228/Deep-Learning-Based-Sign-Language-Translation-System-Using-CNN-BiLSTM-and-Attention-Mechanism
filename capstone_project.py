# ===========================================================
def evaluate(model_components, dataloader, max_len=30):
    cnn, encoder, decoder = model_components
    cnn.eval(); encoder.eval(); decoder.eval()
    total_bleu = 0
    count = 0

    with torch.no_grad():
        for frames, targets in dataloader:
            max_frame_len = max(f.size(0) for f in frames)
            padded_frames = torch.zeros(len(frames), max_frame_len, frames[0].size(1), frames[0].size(2), frames[0].size(3))
            for i, f in enumerate(frames):
                padded_frames[i, :f.size(0)] = f
            frames = padded_frames.to(device)

            batch_size = frames.size(0)

            cnn_feats = cnn(frames)
            enc_out, (h, c) = encoder(cnn_feats)

            hidden = torch.cat((h[-2,:,:], h[-1,:,:]), dim=1).unsqueeze(0)
            cell = torch.cat((c[-2,:,:], c[-1,:,:]), dim=1).unsqueeze(0)

            input_token = torch.tensor([word2idx['<sos>']]*batch_size, device=device)
            outputs_batch = []

            for t in range(max_len):
                output, hidden, cell = decoder(input_token, hidden, cell, enc_out)
                top1 = output.argmax(1)
                if top1[0].item() == word2idx["<eos>"]:
                    break
                outputs_batch.append(top1)
                input_token = top1

            if len(outputs_batch) > 0:
                outputs_batch = torch.stack(outputs_batch, dim=1)
            else:
                outputs_batch = torch.tensor([[]], device=device, dtype=torch.long)

            for i in range(batch_size):
                if outputs_batch.numel() > 0:
                    pred_tokens = [idx2word[idx.item()] if idx.item() < len(idx2word) else "<unk>" for idx in outputs_batch[i] if idx.item() not in [word2idx['<pad>'], word2idx['<eos>']]]
                else:
                    pred_tokens = []

                true_tokens = [idx2word[idx.item()] if idx.item() < len(idx2word) else "<unk>" for idx in targets[i][1:] if idx.item() not in [word2idx['<pad>'], word2idx['<eos>']]]

                if true_tokens and pred_tokens:
                    bleu_score = sentence_bleu([true_tokens], pred_tokens, weights=(0.5,0.5))
                    total_bleu += bleu_score
                    count += 1

    return total_bleu / max(1, count)

