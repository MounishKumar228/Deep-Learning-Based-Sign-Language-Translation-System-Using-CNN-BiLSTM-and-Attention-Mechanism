# Feature extractor: pretrained CNN (ResNet18)
class CNNFeatureExtractor(nn.Module):
    def __init__(self):
        super().__init__()
        base = models.resnet18(weights=models.ResNet18_Weights.DEFAULT)
        self.features = nn.Sequential(*list(base.children())[:-1])  # remove final FC
        self.fc = nn.Linear(base.fc.in_features, 512)

    def forward(self, x):
        with torch.no_grad():
            feats = self.features(x)
        feats = feats.view(feats.size(0), -1)
        return self.fc(feats)

# Encoder: BiLSTM
class BiLSTMEncoder(nn.Module):
    def __init__(self, input_dim=512, hidden_dim=256, num_layers=1):
        super().__init__()
        self.lstm = nn.LSTM(input_dim, hidden_dim, num_layers, batch_first=True, bidirectional=True)

    def forward(self, x):
        outputs, (h, c) = self.lstm(x)
        return outputs, (h, c)

# Attention
class Attention(nn.Module):
    def __init__(self, enc_out_dim, dec_hidden_dim):
        super().__init__()
        # Input size for the linear layer is the sum of the dimensions of the tensors being concatenated
        # These are the decoder's hidden state (dec_hidden_dim) and the encoder's outputs (enc_out_dim)
        self.attn = nn.Linear(dec_hidden_dim + enc_out_dim, dec_hidden_dim)
        self.v = nn.Linear(dec_hidden_dim, 1, bias=False)

    def forward(self, hidden, encoder_outputs):
        # hidden shape: (num_layers * num_directions, batch, hidden_size) -> (1, batch, dec_hidden_dim) from Decoder
        # encoder_outputs shape: (batch, seq_len, enc_out_dim) from Encoder

        # Use the hidden state from the decoder (shape: batch, dec_hidden_dim)
        hidden_state = hidden.squeeze(0) # (batch, dec_hidden_dim)

        # Repeat hidden state across sequence length
        hidden_repeated = hidden_state.unsqueeze(1).repeat(1, encoder_outputs.size(1), 1) # (batch, seq_len, dec_hidden_dim)

        # Concatenate repeated hidden state and encoder outputs
        combined = torch.cat((hidden_repeated, encoder_outputs), dim=2) # (batch, seq_len, dec_hidden_dim + enc_out_dim)

        # Calculate attention energies
        energy = torch.tanh(self.attn(combined)) # (batch, seq_len, dec_hidden_dim)

        # Calculate attention weights
        attention = self.v(energy).squeeze(2) # (batch, seq_len)

        return torch.softmax(attention, dim=1)

# Decoder
class Decoder(nn.Module):
    def __init__(self, output_dim, enc_out_dim, dec_hidden_dim, dropout=0.1):
        super().__init__()
        self.embedding = nn.Embedding(output_dim, dec_hidden_dim)
        # Input to RNN: embedded (dec_hidden_dim) + weighted encoder output (enc_out_dim)
        self.rnn = nn.LSTM(dec_hidden_dim + enc_out_dim, dec_hidden_dim, batch_first=True)
        # Input to FC_out: output from RNN (dec_hidden_dim) + weighted encoder output (enc_out_dim) + embedded (dec_hidden_dim)
        self.fc_out = nn.Linear(dec_hidden_dim + enc_out_dim + dec_hidden_dim, output_dim)
        self.dropout = nn.Dropout(dropout)
        # Attention module
        # Pass the correct dimensions to the Attention module: encoder output dimension and decoder hidden dimension
        self.attention = Attention(enc_out_dim=enc_out_dim, dec_hidden_dim=dec_hidden_dim)

    def forward(self, input, hidden, cell, encoder_outputs):
        # input shape: (batch_size) - single token for teacher forcing or start token
        # hidden shape: (num_layers, batch, hidden_size) -> (1, batch, dec_hidden_dim)
        # cell shape: (num_layers, batch, hidden_size) -> (1, batch, dec_hidden_dim)
        # encoder_outputs shape: (batch, seq_len, enc_out_dim)

        input = input.unsqueeze(1) # (batch_size, 1)
        embedded = self.dropout(self.embedding(input)) # (batch_size, 1, dec_hidden_dim)

        # Calculate attention weights
        # Attention module expects hidden state of shape (batch, dec_hidden_dim) and encoder_outputs (batch, seq_len, enc_out_dim)
        attn_weights = self.attention(hidden, encoder_outputs) # (batch_size, seq_len)

        attn_weights = attn_weights.unsqueeze(1) # (batch_size, 1, seq_len)
        weighted = torch.bmm(attn_weights, encoder_outputs) # (batch_size, 1, enc_out_dim)

        rnn_input = torch.cat((embedded, weighted), dim=2) # (batch_size, 1, dec_hidden_dim + enc_out_dim)

        output, (hidden, cell) = self.rnn(rnn_input, (hidden, cell)) # output: (batch_size, 1, dec_hidden_dim), hidden, cell: (1, batch_size, dec_hidden_dim)

        # Input to fc_out: output.squeeze(1) (batch_size, dec_hidden_dim) + weighted.squeeze(1) (batch_size, enc_out_dim) + embedded.squeeze(1) (batch_size, dec_hidden_dim)
        prediction = self.fc_out(torch.cat((output.squeeze(1), weighted.squeeze(1), embedded.squeeze(1)), dim=1)) # (batch_size, output_dim)

        return prediction, hidden, cell

