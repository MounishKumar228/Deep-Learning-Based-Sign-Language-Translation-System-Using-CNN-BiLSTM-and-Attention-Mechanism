def train(model_components, dataloader, optimizer, criterion, clip=1.0):
    cnn, encoder, decoder = model_components
    cnn.train(); encoder.train(); decoder.train()
    epoch_loss = 0

    for frames, targets in tqdm(dataloader, desc="Training"):
        max_frame_len = max(f.size(0) for f in frames)
        padded_frames = torch.zeros(len(frames), max_frame_len, frames[0].size(1), frames[0].size(2), frames[0].size(3))
        for i, f in enumerate(frames):
            padded_frames[i, :f.size(0)] = f
        frames = padded_frames.to(device)

        max_target_len = max(t.size(0) for t in targets)
        padded_targets = torch.ones(len(targets), max_target_len).long() * word2idx['<pad>']
        for i, t in enumerate(targets):
            padded_targets[i, :t.size(0)] = t
        targets = padded_targets.to(device)

        optimizer.zero_grad()
        batch_size = frames.size(0)

        cnn_feats = cnn(frames)
        enc_out, (h, c) = encoder(cnn_feats)

        hidden = torch.cat((h[-2,:,:], h[-1,:,:]), dim=1).unsqueeze(0)
        cell = torch.cat((c[-2,:,:], c[-1,:,:]), dim=1).unsqueeze(0)

        input_token = torch.tensor([word2idx['<sos>']]*batch_size, device=device)
        loss = 0

        for t in range(1, targets.size(1)):
            output, hidden, cell = decoder(input_token, hidden, cell, enc_out)
            loss += criterion(output, targets[:,t])
            input_token = targets[:,t]

        loss.backward()
        torch.nn.utils.clip_grad_norm_(list(cnn.parameters()) + list(encoder.parameters()) + list(decoder.parameters()), clip)
        optimizer.step()
        epoch_loss += loss.item() / targets.size(1)

    return epoch_loss / len(dataloader)

