def load_phoenix_annotations(gzip_path):
    """Load gzip annotation file using correct encoding."""
    lines = []
    with gzip.open(gzip_path, 'rb') as f:
        raw_data = f.read().decode('latin1', errors='ignore')
        for line in raw_data.split('\n'):
            line = line.strip()
            if line:
                lines.append(line)
    print(f"Loaded {len(lines)} raw lines from annotation file.")
    return lines


def clean_annotations(raw_lines):
    """Remove binary remnants and keep valid German sentences."""
    cleaned = []
    for line in raw_lines:
        line = re.sub(r'[^\x20-\x7EäöüßÄÖÜ]+', ' ', line)  # remove non-printable
        line = re.sub(r'\s+', ' ', line).strip()
        if re.search(r'[a-zA-ZäöüßÄÖÜ]', line) and len(line.split()) > 2:
            cleaned.append(line)
    print(f"Cleaned {len(cleaned)} valid sentences.")
    return cleaned


def build_vocab(sentences, min_freq=2):
    """Build vocabulary from cleaned sentences."""
    words = [w for s in sentences for w in s.split()]
    vocab = ['<pad>', '<sos>', '<eos>', '<unk>']
    for word, count in Counter(words).most_common():
        if count >= min_freq:
            vocab.append(word)
    print(f"Vocab size: {len(vocab)}")
    return vocab


