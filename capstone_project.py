def translate_video(video_path, max_len=30):
    frames = extract_frames(video_path)

    # Add check for empty frames
    if not frames:
        print("No frames extracted from the video. Cannot perform translation.")
        return "Translation failed: No frames extracted."

    # Convert list of numpy arrays to tensor
    frames_tensor = torch.tensor(np.array(frames)).permute(0, 3, 1, 2).float().to(device)

    # Initialize models
    cnn = CNNFeatureExtractor().to(device)
    encoder = BiLSTMEncoder(input_dim=512, hidden_dim=256).to(device) # Encoder hidden_dim=256
    decoder = Decoder(len(vocab), enc_out_dim=512, dec_hidden_dim=512).to(device) # Decoder hidden_dim=512, Encoder output dim = 512

    cnn.eval()
    encoder.eval()
    decoder.eval()

    with torch.no_grad():
        # Process frames through CNN
        cnn_features = cnn(frames_tensor) # (seq_len, 512)
        cnn_features = cnn_features.unsqueeze(0) # Add batch dimension (1, seq_len, 512)

        # Process features through Encoder
        enc_out, (h, c) = encoder(cnn_features) # enc_out: (1, seq_len, 512), h, c: (num_layers*num_directions, 1, hidden_dim) -> (2, 1, 256)

        # Prepare for Decoder
        input_token = torch.tensor([word2idx["<sos>"]], device=device) # (1)
        hidden = c.view(1, 1, -1) # Use final cell state from encoder as initial hidden state for decoder (shape: 1, 1, 512)
        cell = c.view(1, 1, -1) # Use final cell state from encoder as initial cell state for decoder (shape: 1, 1, 512)


        result = []

        for _ in range(max_len):
            # output: (1, vocab_size), hidden, cell: (num_layers, 1, hidden_dim)
            output, hidden, cell = decoder(input_token, hidden, cell, enc_out)
            top1 = output.argmax(1) # (1)
            token = top1.item()

            if token == word2idx["<eos>"]:
                break

            result.append(idx2word.get(token, "<unk>"))
            input_token = top1 # Use predicted token as next input

    return " ".join(result)

